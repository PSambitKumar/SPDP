package com.spdp.bean;

import java.util.List;

public class BlockDistrictBean {
	private String blockName;
	private List<String> distNames;
	
	public String getBlockName() {
		return blockName;
	}
	public void setBlockName(String blockName) {
		this.blockName = blockName;
	}
	public List<String> getDistNames() {
		return distNames;
	}
	public void setDistNames(List<String> distNames) {
		this.distNames = distNames;
	}
	
	

}
